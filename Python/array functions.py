l1 = [1,8,7,2,31,25]
print(l1)

#sorting a list
l1.sort()
print(l1)

#adding something in the end
l1.append(100)
print(l1)


#adding something right after anything
l1.insert(3,500)  #500 will be added after 3rd index
print(l1)


#rearrenging the linst in reverse order
l1.reverse()
print(l1)

#removing any index
l1.pop(2)  # .pop(index number)
print(l1)


#removing anything
l1.remove(2)  # it will remove 2 from the list
print(l1)